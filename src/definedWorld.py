spawnChance = 5


def loadWorld():
    walls = []

    spawnedPositions = []

    y = 0
    summonedPhantoms = 0
    for row in room0:
        x = 0
        for col in row:
            if col == 1:
                e = createSprite(pygame.Vector2(x * 50, y * 50), pygame.Vector2(50, 50), 'src/images/grass.png')
                walls.append(e)
                game.spriteGroups["worldModel"].add(e)
            else:
                if random.randint(0, 100) < spawnChance and summonedPhantoms < 6:
                    p = pygame.Vector2(x * 50, y * 50)

                    if x * 50 < 150 and y * 50 < 150:
                        continue

                    t = False
                    for z in spawnedPositions:
                        if (z - p).magnitude() < 150:
                            t = True
                            break
                    if t:
                        continue

                    summonedPhantoms += 1

                    c = random.choice(['fire', 'water', 'earth', 'air', 'light', 'dark'])
                    
                    e = createSprite(p, pygame.Vector2(50, 50), f'src/images/{c}_phantom.png')

                    e.type = c

                    game.spriteGroups['otherEntities'].add(e)

                    spawnedPositions.append(p)
            x += 1
        y += 1